    /**
     * Create and show a simple notification containing the received FCM message.
     *
     * @param messageBody FCM message body received.
     */

    override fun handleIntent(intent: Intent) {
        Log.d("FCM", "handleIntent ")
        val bundle = intent.extras


        if (bundle != null) {
            for (key in bundle.keySet()) {
                val value = bundle[key]
                Log.d("FCM", "Key: $key Value: $value")
            }

            body = bundle.getString("gcm.notification.body")
            title = bundle.getString("gcm.notification.title")
            eventType = bundle.getString("eventType")



            if (body != null) {
                //sendNotification(body, title, eventType)
                sendNotifications(body, title, eventType)
            }
        }
    }

    private fun sendNotifications(messageBody: String?, messageTitle: String?, messageEvent: String?) {
        val requestCode = 0
        val bundle = Bundle()
        bundle.putString("event_type", messageEvent)
        val intent = Intent(this, MainActivity::class.java)
        intent.addFlags(Intent.FLAG_ACTIVITY_CLEAR_TOP)
        intent.putExtras(bundle)
        //Timber.i("--pendingIntent--$intent")
        val pendingIntent = PendingIntent.getActivity(
            this,
            requestCode,
            intent,
            PendingIntent.FLAG_MUTABLE,
        )

        val channelId = getString(R.string.default_notification_channel_id)
        val defaultSoundUri = RingtoneManager.getDefaultUri(RingtoneManager.TYPE_NOTIFICATION)
        val notificationBuilder = NotificationCompat.Builder(this, channelId)
            .setSmallIcon(R.mipmap.ic_launcher)
            .setContentTitle(messageTitle)
            .setContentText(messageBody)
            .setAutoCancel(true)
            .setSound(defaultSoundUri)
            .addExtras(bundle)
            .setContentIntent(pendingIntent)

        val notificationManager = getSystemService(Context.NOTIFICATION_SERVICE) as NotificationManager

        // Since android Oreo notification channel is needed.
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.O) {
            val channel = NotificationChannel(
                channelId,
                "Channel human readable title",
                NotificationManager.IMPORTANCE_DEFAULT,
            )
            notificationManager.createNotificationChannel(channel)
        }

        val notificationId = 0
        notificationManager.notify(notificationId, notificationBuilder.build())
    }

